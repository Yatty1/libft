#include "ft_printf.h"

int		is_conversion(char c)
{
	if (c == 's' || c == 'S' || c == 'p' || c == 'd'
			|| c == 'D' || c == 'i' || c == 'o' || c == 'O'
			|| c == 'u' || c == 'U' || c == 'x' || c == 'X'
			|| c == 'c' || c == 'C' || c == '%')
		return (1);
	return (0);
}

int		is_stop(char c)
{
	if (c == '.' || c == '%' || c == ' ' || c == '+' || c == '-' || c == '#')
		return (1);
	return (0);
}

int		is_tflag(char c)
{
	if (c == 'h' || c == 'l' || c == 'j' || c == 'z')
		return (1);
	return (0);
}
